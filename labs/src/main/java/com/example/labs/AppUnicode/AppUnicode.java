package com.example.labs.AppUnicode;

public class AppUnicode {

    public static void main(String[] args) {
        System.out.println((char) 64); //ASCI @ (znaki duże i małe alfabetu + cyfry A-Za-z0-9)

        System.out.println("©");        // WIN + .   - można wybrać dowolny znak

        System.out.println("\u2602");   //UNICODE U+2602 - umbrella (znaki alfabetó hiński, łacina, grecki itp + emotikony)


    }
}
